# function for addition
def add(x, y):
    add1 = x + y
    return add1


# function for subtraction
def sub(x, y):
    sub1 = x - y
    return sub1


# function for multiplication
def multi(x, y):
    mul = x * y
    return mul


# function for division
def divi(x, y):
    div = x % y
    return div


# function for modulus
def mod1(x, y):
    mod = x - y
    return mod





